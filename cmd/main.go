	//sv1 := router.Group("/apis/v1/auth/")
	//sv1.Use(common.JWTAuth())
	//{
	//	sv1.GET("/time", common.GetDataByTime)
	//
	//}
	//router.Run(":8081")
	//start and wait for stop signal
	if err := app.Run(); err != nil {
		panic(err)
	}
